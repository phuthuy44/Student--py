class DangNhap:
     def __init__(self,tenDangNhap,matKhau):
          self.tenDangNhap = tenDangNhap
          self.matKhau = matKhau
     def setTenDangNhap(self,tenDangNhap):
          self.tenDangNhap = tenDangNhap
     def getTenDangNhap(self):
          return self.tenDangNhap
     def setMatKhau(self,matKhau):
          self.matKhau = matKhau
     def getMatKhau(self):
          return self.matKhau
